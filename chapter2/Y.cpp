#include <iostream>
using namespace std;

int main() {

	// Binary
	cout << 0b01011001 << endl << char (0b01011001);
	

	// Octal
	cout << 0131 << " ";
	cout << char(0131) << endl;

	// Decimal
	cout << 89 << " ";
	cout << char(89) << endl;

	// Hexadecimal
	cout << 0X59 << " ";
	cout << char(0X59) << endl;
}